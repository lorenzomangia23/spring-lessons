create table cars (
                      id int not null AUTO_INCREMENT,
                      name varchar(100) not null,
                      quantity int,
                      brand varchar(100),
                      model varchar(100),
                      price varchar(100),
                      PRIMARY KEY ( id )
);

create table bikes (
                      id int not null AUTO_INCREMENT,
                      name varchar(100) not null,
                      quantity int,
                      brand varchar(100),
                      model varchar(100),
                      price varchar(100),
                      PRIMARY KEY ( id )
);